def add_numbers(x, y):
    """
    Adds two numbers and returns their sum.
    
    Parameters:
    x (int/float): The first number.
    y (int/float): The second number.
    
    Returns:
    int/float: The sum of the two numbers.
    """
    return x + y

# Example usage
result = add_numbers(3, 5)
print(result) # Output: 8
